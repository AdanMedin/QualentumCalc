from math import sqrt

# Operaciones:

def suma(a, b):
 return a + b

def resta(a, b):
 return a - b

def multiplicacion(a, b):
 return a * b

def division(a, b):
 return a / b

def raizCuadrada(a):
 return sqrt(a)

if __name__ == "__main__":
 print(suma(5, 3))
 print(resta(5, 3))
 print(multiplicacion(4, 2))
 print(division(4, 2))
 print(raizCuadrada(3))